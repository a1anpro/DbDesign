package com.bean;

public class DetailedOrderBean {
	private String bookname;
	private double boughtcost;
	private int boughtnum;
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public double getBoughtcost() {
		return boughtcost;
	}
	public void setBoughtcost(double boughtcost) {
		this.boughtcost = boughtcost;
	}
	public int getBoughtnum() {
		return boughtnum;
	}
	public void setBoughtnum(int boughtnum) {
		this.boughtnum = boughtnum;
	}
	
	
}
